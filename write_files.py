# Write Dockerfile
with open('Dockerfile', 'w') as f:
    f.write('FROM python:3.10-slim\n')
    f.write('\n')
    f.write('WORKDIR /app\n')
    f.write('\n')
    f.write('COPY requirements.txt .\n')
    f.write('RUN pip install --no-cache-dir -r requirements.txt\n')
    f.write('\n')
    f.write('COPY src/ ./src/\n')
    f.write('COPY data/processed_data/ ./data/processed_data/\n')
    f.write('\n')
    f.write('ENV QDRANT_URL=http://qdrant:6333\n')
    f.write('\n')
    f.write('EXPOSE 8000\n')
    f.write('\n')
    f.write('CMD ["uvicorn", "src.api:app", "--host", "0.0.0.0", "--port", "8000"]\n')
print('Dockerfile done')

