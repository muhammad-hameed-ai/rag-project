# Write docker-compose.yml
with open('docker-compose.yml', 'w') as f:
    f.write('services:\n')
    f.write('  qdrant:\n')
    f.write('    image: qdrant/qdrant\n')
    f.write('    container_name: rag_qdrant\n')
    f.write('    ports:\n')
    f.write('      - "6333:6333"\n')
    f.write('    volumes:\n')
    f.write('      - qdrant_storage:/qdrant/storage\n')
    f.write('    healthcheck:\n')
    f.write('      test: ["CMD-SHELL", "curl -f http://localhost:6333/health || exit 1"]\n')
    f.write('      interval: 30s\n')
    f.write('      timeout: 10s\n')
    f.write('      retries: 3\n')
    f.write('\n')
    f.write('  api:\n')
    f.write('    build: .\n')
    f.write('    container_name: rag_api\n')
    f.write('    ports:\n')
    f.write('      - "8000:8000"\n')
    f.write('    environment:\n')
    f.write('      - QDRANT_URL=http://qdrant:6333\n')
    f.write('    depends_on:\n')
    f.write('      qdrant:\n')
    f.write('        condition: service_healthy\n')
    f.write('    restart: unless-stopped\n')
    f.write('\n')
    f.write('volumes:\n')
    f.write('  qdrant_storage:\n')
print('docker-compose.yml done')

