# deployment.yaml
with open('k8s/deployment.yaml', 'w') as f:
    f.write('apiVersion: apps/v1\n')
    f.write('kind: Deployment\n')
    f.write('metadata:\n')
    f.write('  name: rag-api\n')
    f.write('  labels:\n')
    f.write('    app: rag-api\n')
    f.write('spec:\n')
    f.write('  replicas: 2\n')
    f.write('  selector:\n')
    f.write('    matchLabels:\n')
    f.write('      app: rag-api\n')
    f.write('  template:\n')
    f.write('    metadata:\n')
    f.write('      labels:\n')
    f.write('        app: rag-api\n')
    f.write('    spec:\n')
    f.write('      containers:\n')
    f.write('      - name: rag-api\n')
    f.write('        image: vision2030-rag-api:1.0.0\n')
    f.write('        imagePullPolicy: Never\n')
    f.write('        ports:\n')
    f.write('        - containerPort: 8000\n')
    f.write('        env:\n')
    f.write('        - name: QDRANT_URL\n')
    f.write('          valueFrom:\n')
    f.write('            configMapKeyRef:\n')
    f.write('              name: rag-config\n')
    f.write('              key: qdrant_url\n')
    f.write('        resources:\n')
    f.write('          requests:\n')
    f.write('            memory: "256Mi"\n')
    f.write('            cpu: "250m"\n')
    f.write('          limits:\n')
    f.write('            memory: "512Mi"\n')
    f.write('            cpu: "500m"\n')
    f.write('        livenessProbe:\n')
    f.write('          httpGet:\n')
    f.write('            path: /health\n')
    f.write('            port: 8000\n')
    f.write('          initialDelaySeconds: 30\n')
    f.write('          periodSeconds: 10\n')
    f.write('          failureThreshold: 3\n')
    f.write('        readinessProbe:\n')
    f.write('          httpGet:\n')
    f.write('            path: /health\n')
    f.write('            port: 8000\n')
    f.write('          initialDelaySeconds: 20\n')
    f.write('          periodSeconds: 5\n')
    f.write('          failureThreshold: 3\n')
print('deployment.yaml written')

# service.yaml
with open('k8s/service.yaml', 'w') as f:
    f.write('apiVersion: v1\n')
    f.write('kind: Service\n')
    f.write('metadata:\n')
    f.write('  name: rag-api-service\n')
    f.write('spec:\n')
    f.write('  selector:\n')
    f.write('    app: rag-api\n')
    f.write('  ports:\n')
    f.write('  - protocol: TCP\n')
    f.write('    port: 80\n')
    f.write('    targetPort: 8000\n')
    f.write('  type: NodePort\n')
print('service.yaml written')

# configmap.yaml
with open('k8s/configmap.yaml', 'w') as f:
    f.write('apiVersion: v1\n')
    f.write('kind: ConfigMap\n')
    f.write('metadata:\n')
    f.write('  name: rag-config\n')
    f.write('data:\n')
    f.write('  qdrant_url: "http://host.minikube.internal:6333"\n')
    f.write('  model_name: "llama3.2:1b"\n')
    f.write('  collection_name: "saudi_vision_2030"\n')
print('configmap.yaml written')

# hpa.yaml
with open('k8s/hpa.yaml', 'w') as f:
    f.write('apiVersion: autoscaling/v2\n')
    f.write('kind: HorizontalPodAutoscaler\n')
    f.write('metadata:\n')
    f.write('  name: rag-api-hpa\n')
    f.write('spec:\n')
    f.write('  scaleTargetRef:\n')
    f.write('    apiVersion: apps/v1\n')
    f.write('    kind: Deployment\n')
    f.write('    name: rag-api\n')
    f.write('  minReplicas: 2\n')
    f.write('  maxReplicas: 10\n')
    f.write('  metrics:\n')
    f.write('  - type: Resource\n')
    f.write('    resource:\n')
    f.write('      name: cpu\n')
    f.write('      target:\n')
    f.write('        type: Utilization\n')
    f.write('        averageUtilization: 60\n')
print('hpa.yaml written')

