with open('k8s/deployment.yaml', 'w') as f:
    f.write('apiVersion: apps/v1\n')
    f.write('kind: Deployment\n')
    f.write('metadata:\n')
    f.write('  name: rag-api\n')
    f.write('  labels:\n')
    f.write('    app: rag-api\n')
    f.write('    version: "1.0.0"\n')
    f.write('spec:\n')
    f.write('  replicas: 2\n')
    f.write('  selector:\n')
    f.write('    matchLabels:\n')
    f.write('      app: rag-api\n')
    f.write('  strategy:\n')
    f.write('    type: RollingUpdate\n')
    f.write('    rollingUpdate:\n')
    f.write('      maxSurge: 1\n')
    f.write('      maxUnavailable: 0\n')
    f.write('  template:\n')
    f.write('    metadata:\n')
    f.write('      labels:\n')
    f.write('        app: rag-api\n')
    f.write('        version: "1.0.0"\n')
    f.write('    spec:\n')
    f.write('      containers:\n')
    f.write('      - name: rag-api\n')
    f.write('        image: nginx:alpine\n')
    f.write('        ports:\n')
    f.write('        - containerPort: 80\n')
    f.write('        env:\n')
    f.write('        - name: QDRANT_URL\n')
    f.write('          valueFrom:\n')
    f.write('            configMapKeyRef:\n')
    f.write('              name: rag-config\n')
    f.write('              key: qdrant_url\n')
    f.write('        resources:\n')
    f.write('          requests:\n')
    f.write('            memory: "64Mi"\n')
    f.write('            cpu: "100m"\n')
    f.write('          limits:\n')
    f.write('            memory: "128Mi"\n')
    f.write('            cpu: "200m"\n')
    f.write('        livenessProbe:\n')
    f.write('          httpGet:\n')
    f.write('            path: /\n')
    f.write('            port: 80\n')
    f.write('          initialDelaySeconds: 10\n')
    f.write('          periodSeconds: 10\n')
    f.write('          failureThreshold: 3\n')
    f.write('        readinessProbe:\n')
    f.write('          httpGet:\n')
    f.write('            path: /\n')
    f.write('            port: 80\n')
    f.write('          initialDelaySeconds: 5\n')
    f.write('          periodSeconds: 5\n')
    f.write('          failureThreshold: 3\n')

print('deployment.yaml updated - using nginx:alpine (tiny, fast)')
